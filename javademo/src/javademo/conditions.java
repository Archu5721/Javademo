package javademo;

public class conditions {
	public static void main(String[] args) {
		int var1 =10;
		boolean isTrue = true;
		
		if (var1 == 10) {
			System.out.println("Yes");
		}
		else {
			System.out.println("No");
		}
		
		
		if(isTrue = false) { // the condition becomes false
			System.out.println("t");
		}
		else {
			System.out.println("f");
		}
	}

}
